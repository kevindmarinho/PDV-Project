    public void listar(){
        FornecedorDAO dao  = new FornecedorDAO();
        List<Fornecedores> lista = dao.listarFornecedores();
        DefaultTableModel dados = (DefaultTableModel) tabelaFornecedor.getModel();
        dados.setNumRows(0);
        
        for (Fornecedores c : lista){
           dados.addRow(new Object[]{
            c.getId(),
            c.getRazao(),
            c.getCnpj(),
            c.getEmail(),
            c.getTelefone(),
            c.getCep(),
            c.getEndereco(),
            c.getNumero(),
            c.getBairro(),
            c.getCidade(),
            c.getUf()
            });
        }
        
    }

