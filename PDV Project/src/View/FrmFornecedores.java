    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // botao excluir
                int op;
                
                op = JOptionPane.showConfirmDialog(null, "Você tem certeza que seja excluir o Fornecedor ?");
                if (op==0){
                    Fornecedores obj = new Fornecedores();

                    try {
                        obj.setId(Integer.parseInt(tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 0).toString()));

                        FornecedorDAO dao = new FornecedorDAO();

                        dao.excluirFornecedor(obj);
                        limparTexto();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "DEU B.O" + e);
                    }
                    
                }
    }//GEN-LAST:event_jButton5MouseClicked

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // botao para salvar 
            if(checarCampos()== 0){
            Fornecedores obj = new Fornecedores ();           


            obj.setRazao(txtRazao.getText());
            obj.setCnpj(txtCnpj.getText());
            obj.setEmail(txtEmail.getText());
            obj.setTelefone(txtNumero1.getText());
            obj.setCep(txtCep.getText());
            obj.setEndereco(txtEndereco.getText());
            obj.setNumero(Integer.parseInt(txtNumero.getText()));
            obj.setBairro(txtBairro.getText());
            obj.setCidade(txtCidade.getText());
            obj.setUf(cbUf.getSelectedItem().toString());
            
            FornecedorDAO dao = new FornecedorDAO();
                       
            try {
                obj.setId(Integer.parseInt(txtCodigo.getText()));               
                dao.alterarFornecedor(obj);
                limparTexto();
           } catch (Exception e) {               
                dao.cadastrarFornecedor(obj);
                limparTexto();
           }           

       }
    }//GEN-LAST:event_btnSalvarActionPerformed

