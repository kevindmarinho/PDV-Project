      private int checarCampos(){
          //checar se os campos estao vazios 
          int validaCampo = 0;
          String camposVazios = "CAMPOS OBRIGATÓRIOS : \n";
          
          String Nome = txtNome.getText().toString();
          String Cpf = txtCpf.getText().toString();
          String Email = txtEmail.getText().toString();
          String Numero1 = txtNumero1.getText().toString();
          String Cep = txtCep.getText().toString();
          String Endereco = txtEndereco.getText().toString();
          String Numero = txtNumero.getText().toString();
          String Bairro = txtBairro.getText().toString();
          String Cidade = txtCidade.getText().toString();
          String Senha = txtSenha.getText().toString();
          String Cargo = txtCargo.getText().toString();
          String Nivel_acesso = (cbNivel.getSelectedItem().toString());
          
        if (Nome.equals("")) {
            validaCampo = 1;
            camposVazios += "\nNome";
        }
        if (Cpf.equals("")) {
            validaCampo = 1;
            camposVazios += "\nCPF";
        }
        if (Email.equals("")) {
            validaCampo = 1;
            camposVazios += "\nEmail";
        }
        if (Numero1.equals("")) {
            validaCampo = 1;
            camposVazios += "\nCelular";
        }
        if (Cep.equals("")) {
            validaCampo = 1;
            camposVazios += "\nCEP";
        }
        if (Endereco.equals("")) {
            validaCampo = 1;
            camposVazios += "\nEndereço";
        }
        if (Numero.equals("")) {
            validaCampo = 1;
            camposVazios += "\nNúmero";
        }
        if (Bairro.equals("")) {
            validaCampo = 1;
            camposVazios += "\nBairro";
        }
        if (Cidade.equals("")) {
            validaCampo = 1;
            camposVazios += "\nCidade";
        }
        if (Senha.equals("")) {
            validaCampo = 1;
            camposVazios += "\nSenha";
        }
        if (Cargo.equals("")) {
            validaCampo = 1;
            camposVazios += "\nCargo";
        }
        if (Nivel_acesso.equals(".")) {
            validaCampo = 1;
            camposVazios += "\nNível de acesso";
        }
        if (validaCampo == 1) {
            JOptionPane.showMessageDialog(null, camposVazios);
        }
        return validaCampo;
    }

